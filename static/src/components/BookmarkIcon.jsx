export function BookmarkIcon({ bookmark, categoryColor }) {
  const { icon_type, icon_url, symbol_name, title } = bookmark;
  const color = categoryColor || '#8b5cf6';

  // Waterfall logic:
  // 1. Custom icon URL
  if (icon_type === 'custom' && icon_url) {
    return (
      <div className="bookmark-icon" style={{ '--category-color': color }}>
        <img
          src={icon_url}
          alt=""
          loading="lazy"
        />
      </div>
    );
  }

  // 2. Symbol/icon name
  if (icon_type === 'symbol' && symbol_name) {
    const iconComponent = HERO_ICONS[symbol_name.toLowerCase()];
    if (iconComponent) {
      return (
        <div className="bookmark-icon" style={{ '--category-color': color }}>
          {iconComponent}
        </div>
      );
    }
  }

  // 3. Generated placeholder (first letter on colored background)
  if (icon_type === 'generated') {
    const firstLetter = title?.charAt(0) || '?';
    return (
      <div
        className="bookmark-icon generated"
        style={{ '--category-color': color }}
      >
        {firstLetter}
      </div>
    );
  }

  // 4. Default: Bookmark symbol
  return (
    <div className="bookmark-icon" style={{ '--category-color': color }}>
      <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2} stroke="currentColor">
        <path strokeLinecap="round" strokeLinejoin="round" d="M17.593 3.322c1.1.128 1.907 1.077 1.907 2.185V21L12 17.25 4.5 21V5.507c0-1.108.806-2.057 1.907-2.185a48.507 48.507 0 0 1 11.186 0Z" />
      </svg>
    </div>
  );
}
